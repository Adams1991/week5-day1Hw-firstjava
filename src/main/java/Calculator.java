public class Calculator {

    public int add(int a, int b) {
        return a + b;

    }

    public int subtract(int i, int i1) {
        return i - i1;
    }

    public int multiply(int i, int i1) {
        return i*i1;
    }

    public int division(int i, int i1) {
        return i/i1;
    }
}
